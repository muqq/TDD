#import "RecordTableViewController.h"
#import "RecordManager.h"
#import "RecordTableViewCell.h"

@interface RecordTableViewController ()

@property (nonatomic) NSArray *records;

@end

@implementation RecordTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.records = [[RecordManager sharedManager] records];
    [self.tableView reloadData];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.records.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    RecordTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"record_cell" forIndexPath:indexPath];
    cell.recordLabel.text = self.records[indexPath.row];
    return cell;
}


